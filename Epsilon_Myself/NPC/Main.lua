function map_position()
--Define Map Length (always 16128)
maplen = 16128
--Define In-Game Coords
igX = gv("x")
igY = gv("y")
--Divide In-Game Coords by Map Length
--This gives us the map name m00X_00Y        
mpX = igX / maplen
mpY = igY / maplen
--Format the map name
message(mpX)
message(mpY)
end


